package tfc.dynamic_rendering.API;

public class TextureCoords {
	double tx1;
	double tx2;
	double tx3;
	double tx4;
	double ty1;
	double ty2;
	double ty3;
	double ty4;
	
	public TextureCoords(double tx1, double tx2, double tx3, double tx4, double ty1, double ty2, double ty3, double ty4) {
		this.tx1 = tx1;
		this.tx2 = tx2;
		this.tx3 = tx3;
		this.tx4 = tx4;
		this.ty1 = ty1;
		this.ty2 = ty2;
		this.ty3 = ty3;
		this.ty4 = ty4;
	}
}
